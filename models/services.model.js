const mongoose = require("mongoose");

const ServicesSchema = mongoose.Schema({
  icon: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: String,
    required: true,
  },
});

const ServiceModel = mongoose.model("services", ServicesSchema);
module.exports = ServiceModel;
